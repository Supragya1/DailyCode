public class unaryoperator_14 {
    public static void main(String[]args){
    
        int a= 10;
        int b = a++;
        System.out.println(a);
        System.out.println(b);

        int c = 10;
        int d =++c;
        System.out.println(c);
        System.out.println(d);
    }
}
